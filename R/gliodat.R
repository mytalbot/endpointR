#' Rat Body Weight Data Glioblastoma Model
#'
#' Rat body weight data from a intracranial Glioblastoma model. 63 unique animals in three treatment groups (Control,
#' Microinjection and Resection) were monitored on a daily basis. The general model indicates that animal well-being is not substantially
#' impaired until a certain time-point at around 12-16 days is reached. Here, most animals decompensate rapidly and had to be
#' euthanized because the defined humane endpoint for the study was reached. The maximum follow-up is 28 days, although most animals had to
#' be euthanized well before that. In the resection group day 8 is the day of operation. These animals survive longer but a small drop in
#' body weight can be seen after day 8, together with rapid recovery.
#'
#' @docType data
#'
#' @usage data(gliodat)
#'
#' @format A 63 x 31 data frame with post-op data.
#'
#' @keywords datasets
#'
#' @examples
#' data(gliodat)
#' head(gliodat)
